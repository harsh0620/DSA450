Node *insertAtEnd(Node *head, int x)
{
    Node *temp = new Node(x);
    if (head == NULL)
    {
        return temp;
    }
    Node *curr = head;
    while (curr->next != NULL)
    {
        curr = curr->next;
    }
    curr->next = temp;
    return head;
}

Node *findIntersection(Node *head1, Node *head2)
{
    Node *head = NULL;
    while (head1 != NULL && head2 != NULL)
    {
        int x = head1->data;
        int y = head2->data;
        if (x == y)
        {
            head = insertAtEnd(head, head1->data);
            head1 = head1->next;
            head2 = head2->next;
        }
        else if (x < y)
        {
            head1 = head1->next;
        }
        else
        {
            head2 = head2->next;
        }
    }
    return head;
}